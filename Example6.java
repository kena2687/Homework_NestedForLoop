package Homework_NestedForLoop;

public class Example6 {

    public static void main(String[] args) {

        for (int i = 2; i <= 20; i += 2) {

            System.out.println("Even Number: " + i);
        }
        for (int j = 1; j <= 20; j += 2) {
            System.out.println("Odd Number: " + j);

        }
    }

}